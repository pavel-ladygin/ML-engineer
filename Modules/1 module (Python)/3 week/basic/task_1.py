"""Функции. Аргументы 1
Допустим, ваша компания каждый месяц выделяет некоторую сумму денег на подарки сотрудникам ко дню рождения.
Каждый месяц эта сумма разная. Также у вас есть список дней рождения сотрудников.
Вам нужно написать удобный инструмент для того, чтобы сотрудники могли понять, сколько денег им ждать на день рождения, если известно,
какой бюджет выделяется на месяц и для какого месяца производится подсчет. Для этого напишите функцию gift_count, которая будет принимать:

бюджет, который выделяется компанией на месяц
номер месяца, на который нужно произвести расчет
словарь, где ключи - это имена сотрудников, а значения - datetime.date с датой рождения сотрудника.
Функция должна вывести на экран фразу (цифры должны быть подставлены в соответствии со значениями аргументов):

Именинники в месяце 5: Иванов Иван Иванович (01.05.1989), Петров Петр Петрович (06.05.1998). При бюджете 20000 они получат по 10000 рублей.

Если в ответе получается не целое число, округлите его до меньшего целого. Именинники должны быть выведены в порядке дня рождения по возрастанию
(не полной даты рождения, а именно дня). Если в выбранном месяце нет именинников, должна быть выведена строка "В этом месяце нет именинников."



Формат ввода
birthdays = {"Иванов Иван Иванович": datetime.date(1989, 5, 1), "Петров Петр Петрович": datetime.date(1998, 5, 6)}

gift_count(20000, 5, birthdays)

gift_count(budget=20000, month=5, birthdays=birthdays)



Формат вывода
Именинники в месяце 5: Иванов Иван Иванович (01.05.1989), Петров Петр Петрович (06.05.1998). При бюджете 20000 они получат по 10000 рублей.

 """

import datetime
import math


def gift_count(budget, month, birthdays):
    month_birthdays = []

    for name, birthday in birthdays.items():
        if birthday.month == month:
            month_birthdays.append((name, birthday))

    if not month_birthdays:
        print("В этом месяце нет именинников.")
        return

    month_birthdays.sort(key=lambda item: item[1].day)  # Сортировка по дню рождения

    names = []
    date_strings = []
    for name, birthday in month_birthdays:
        names.append(name)
        date_strings.append(birthday.strftime("%d.%m.%Y"))

    gift_amount = math.floor(budget / len(month_birthdays))

    names_str = ", ".join([f"{name} ({date_str})" for name, date_str in zip(names, date_strings)])
    print(f"Именинники в месяце {month}: {names_str}. При бюджете {budget} они получат по {gift_amount} рублей.")